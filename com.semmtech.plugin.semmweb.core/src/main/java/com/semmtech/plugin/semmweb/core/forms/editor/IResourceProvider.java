package com.semmtech.plugin.semmweb.core.forms.editor;


import com.hp.hpl.jena.ontology.OntClass;
import com.hp.hpl.jena.ontology.OntModel;
import com.hp.hpl.jena.ontology.OntResource;
import com.hp.hpl.jena.rdf.model.Resource;


public interface IResourceProvider {

    public Resource getResource();

    public String getResourceURI();

    public OntResource getOntResource();

    /**
     * Returns an OntResource retrieved from the given model, based on the
     * resource associated with the current FormPage.
     * 
     * @param model
     *            model from which resource such be retrieved.
     * @return resource retrieved from the specified model, or null.
     */
    public OntResource getOntResourceFromModel(OntModel model);

    public boolean isAnon();

    public boolean isClass();

    public OntClass asClass();
}
